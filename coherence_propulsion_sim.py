def sigma_entropy(C, A=0.3):
    """Entropy generation rate."""
    return 0.05 + 0.2 * (1 - C) + 0.2 * A


def run_cmd_spatial(C_field, t_end=100, dt=0.1, pulse_factor=1.0):
    """
    Run C-M-D dynamics at every grid point simultaneously.
    C_field is the coherence generated by the triangles.
    n, M, D evolve at each point.
    pulse_factor: ionic reservoir amplification (>1 = pulsed mode)

    Returns: final n, M, D, rho_dm fields + time histories at center
    """
    n_steps = int(t_end / dt)

    # Initialize — uniform "vacuum" conditions
    n = np.full_like(C_field, 1.0)      # entropy loops everywhere (dark matter)
    M = np.full_like(C_field, 0.01)     # minimal memory
    D = np.full_like(C_field, 3.0)      # 3D spacetime

    # Ambient adversity (space environment)
    A = 0.3

    # Time histories at center
    cx, cy = NX//2, NY//2
    n_hist = []
    rho_hist = []
    D_hist = []

    for step in range(n_steps):
        # Effective coherence — pulsed by reservoir
        # Pulse creates periodic coherence spikes that hit harder than continuous
        if pulse_factor > 1:
            pulse = 1.0 + (pulse_factor - 1.0) * (0.5 + 0.5 * np.sin(step * dt * 2 * np.pi / 5.0))
        else:
            pulse = 1.0

        C = np.clip(C_field * pulse, 0, 10)  # cap at 10 to prevent blowup

        sig = sigma_entropy(np.clip(C, 0, 1), A)

        # dn/dt = s*sig - delta*C*n  (coherence kills loops)
        # Key: C is NOT clipped for the killing term — stronger C kills more loops
        dn = S_PARAM * sig - DELTA * C * n
        n = np.maximum(n + dn * dt, 0)

        # dM/dt = kappa*C*(1-M) - lambda*sig*M
        dM = KAPPA * np.clip(C, 0, 1) * (1 - M) - LAMBDA * sig * M
        M = np.clip(M + dM * dt, 0, 1)

        # dD/dt = -alpha*(D-2) - gamma*n + beta*M
        dD = -ALPHA * (D - 2) - GAMMA * n + BETA * M
        D = np.maximum(D + dD * dt, 2.0)

        if step % 10 == 0:
            n_hist.append(n[cy, cx])
            rho_hist.append(EPS * n[cy, cx])
            D_hist.append(D[cy, cx])

    rho_dm = EPS * n  # dark matter density

    return n, M, D, rho_dm, np.array(n_hist), np.array(rho_hist), np.array(D_hist)


# ============================================================
# GRAVITATIONAL POTENTIAL FROM DARK MATTER
# ============================================================

def grav_potential(rho_dm, dx):
    """
    Gravitational potential from dark matter distribution.
    Simplified: phi ~ integral of rho/r.
    Using convolution with 1/r kernel.
    """
    # Build 1/r kernel
    kernel = np.zeros_like(rho_dm)
    cx, cy = NX//2, NY//2
    for i in range(NX):
        for j in range(NY):
            r = np.sqrt((i - cx)**2 + (j - cy)**2) * dx
            if r > dx * 0.5:
                kernel[i, j] = 1.0 / r

    # Convolve (simplified — FFT would be faster but this is clearer)
    from scipy.signal import fftconvolve
    phi = -fftconvolve(rho_dm, kernel, mode='same') * dx**2

    return phi


def displacement_from_potential(phi, dx):
    """
    Gravitational acceleration = -gradient(phi).
    Net force on the ship (at center) from the potential gradient.
    """
    # Gradient at center
    cx, cy = NX//2, NY//2

    # Central difference
    dphi_dx = (phi[cx+1, cy] - phi[cx-1, cy]) / (2 * dx)
    dphi_dy = (phi[cx, cy+1] - phi[cx, cy-1]) / (2 * dx)

    # Force = -grad(phi)
    Fx = -dphi_dx
    Fy = -dphi_dy

    return Fx, Fy, np.sqrt(Fx**2 + Fy**2)


