# TRIANGLE COHERENCE FIELD
# ============================================================

def triangle_coherence_field(X, Y, tracks, tilt_direction=0.0):
    """
    Coherence field generated by three triangles on maglev tracks.
    Each vertex creates a coherence pulse that falls off with distance.
    Tilt_direction rotates the asymmetry (steering).

    Returns: C_field (NX x NY) — local coherence value 0 to 1
    """
    C_field = np.zeros_like(X)

    for track_R, n_verts, amplitude in tracks:
        for k in range(n_verts):
            # Vertex position on track (with tilt for asymmetry)
            angle = k * 2 * np.pi / n_verts + tilt_direction
            vx = track_R * np.cos(angle)
            vy = track_R * np.sin(angle)

            # Coherence pulse: Gaussian centered on vertex position
            # Width scales with track radius
            sigma = track_R * 0.4
            dist_sq = (X - vx)**2 + (Y - vy)**2
            C_field += amplitude * np.exp(-dist_sq / (2 * sigma**2))

    # No normalization — raw coherence values matter
    # Higher amplitude triangles = stronger field = more DM erasure
    return C_field


def asymmetric_coherence(X, Y, tracks, steer_angle=0.0, asymmetry=0.3):
    """
    Coherence field with built-in asymmetry for directional displacement.
    The field is stronger in the steer_angle direction (more coherence ahead).
    Asymmetry parameter controls how much stronger one side is.
    """
    C_base = triangle_coherence_field(X, Y, tracks, tilt_direction=0)

    # Apply directional bias — more coherence in the steer direction
    bias = 1.0 + asymmetry * (X * np.cos(steer_angle) + Y * np.sin(steer_angle)) / (GRID_SIZE/2)
    bias = np.clip(bias, 0.5, 1.5)

    C_field = C_base * bias
    # Raw values — amplitude controls strength
    return C_field


